<template>
  <div class="spec-preview">
    <img :src="curObj.imgUrl" />
    <div class="event" @mousemove="handle"></div>
    <div class="big" >
      <img :src="curObj.imgUrl" ref="big"/>
    </div>
    <div class="mask" ref="mask"></div>
  </div>
</template>

<script lang="ts">
import { defineComponent,computed,ref} from 'vue';
import {useStore} from "vuex";
export default defineComponent({
  name: 'Zoom',
  props:['curImg'],
  setup(props){
    const store = useStore()
    const mask = ref(null)
    const big = ref(null)
    const skuInfo = computed(()=>store.getters["detail/skuInfo"])
    const imageList = computed(()=>skuInfo.value.skuImageList||[])
    const curObj = computed(()=>imageList.value[props.curImg]||{})
    const handle = (event)=>{
      let left = event.offsetX - mask.value.offsetWidth/2
      let top = event.offsetY - mask.value.offsetHeight/2
      if(left<0){
        left = 0
      }
      else if(left>=200){
        left=200
      }
      if(top<0){
        top = 0
      }
      else if(top>=200){
        top = 200
      }
      mask.value.style.top = top+'px'
      big.value.style.top = -2*top+'px'
      mask.value.style.left = left+'px'
      big.value.style.left = -2*left+'px'
    }
    return {
      curObj,
      mask,
      handle,
      big
    }
  }
});
</script>

