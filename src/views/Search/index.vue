<script lang="ts">
import {defineComponent, onMounted, onBeforeMount, watch, reactive, ref,computed} from 'vue';
import {useStore} from "vuex";
import {useRouter} from "vue-router";
import {useGetters} from "@/api/store";
import SearchSelector from './SearchSelector/SearchSelector.vue'
export default defineComponent({
  name: 'Search',
  components: {SearchSelector},
  setup(){
    //初始化路由和store等vue工具
    const store = useStore()
    const router = useRouter()
    const route = router.currentRoute
    //data
    const data = reactive(
        {
          "category1Id": '',
          "category2Id": "",
          "category3Id": "",
          "categoryName": "",
          "keyword": "",
          "order": "1:desc",
          "pageNo": 1,
          "pageSize": 10,
          "props": [],
          "trademark": ""
        }
    )
    const arrow = {asc:'⬆',desc:'⬇'}
    const arrowZH = ref('desc')
    const arrowXL = ref('desc')
    //computed
    const mapGetter = useGetters('search',['goodsList'])
    const list = computed(()=>store.state.search.list)
    //methods
    //获取商品数据
    const getList = ()=>{
      store.dispatch('search/getList',data)
    }
    //移除query参数
    const removeQuery = ()=>{
      data.categoryName = undefined
      data.category3Id = undefined
      data.category2Id = undefined
      data.category1Id = undefined
      router.push({name:'search',params:route.value.params})
    }
    //移除params参数
    const removeParams = ()=>{
      data.keyword = undefined
      router.push({name:'search',query:route.value.query})
    }
    //searchSelector相关操作
    //获取trademark参数以及移除操作
    const getTrademark = (id,name)=>{
      let trademark = id+':'+name
      data.trademark = trademark
      getList()
    }
    const removeTrademark = ()=>{
      data.trademark = undefined
      getList()
    }
    //排序
    const changeArrow = (num)=>{
      if (num == 1){
        arrowZH.value = arrowZH.value=='desc'?'asc':'desc'
        data.order = num+':'+ arrowZH.value
      }
      else if(num == 2){
        arrowXL.value = arrowXL.value=='desc'?'asc':'desc'
        data.order = num+':'+ arrowXL.value
      }
      getList()
    }
    //获取属性以及移除操作
    const getAttrs = (id,value,name)=>{
      let attrs = id+':'+value+':'+name
      if (data.props.indexOf(attrs)==-1){
        data.props.push(attrs)
      }
      getList()
    }
    const removeAttrs = (attrs) => {
      data.props = data.props.filter((item)=>item!=attrs)
      getList()
    }
    //watch
    watch(()=>route,(newroute,oldroute)=>{
      let query = route.value.query
      let params = route.value.params
      Object.assign(data,query,params)
      getList()
      //每次请求结束将id清空，否则将影响后续更新
      data.category3Id=''
      data.category2Id=''
      data.category1Id=''
    },{deep:true})
    watch(()=>data.pageNo,(newvalue,oldvalue)=>{
      getList()
    })
    //生命周期函数
    onBeforeMount(()=>{
      let query = route.value.query
      let params = route.value.params
      Object.assign(data,query,params)
    })
    onMounted(()=>{
      getList()
    })
    return{
      ...mapGetter,
      removeQuery,
      removeParams,
      removeTrademark,
      removeAttrs,
      data,
      arrow,
      getTrademark,
      getAttrs,
      changeArrow,
      arrowZH,
      arrowXL,
      list
    }
  }
})
</script>


